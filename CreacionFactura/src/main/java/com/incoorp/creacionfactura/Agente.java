package com.incoorp.creacionfactura;

/**
 *
 * @author michael
 */
public class Agente {
    String nombre;
    String nit;
    String email;
    int telefono;

    public Agente() {
    }

    public Agente(String nombre, String nit, String email, int telefono) {
        this.nombre = nombre;
        this.nit = nit;
        this.email = email;
        this.telefono = telefono;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNit() {
        return nit;
    }

    public void setNit(String nit) {
        this.nit = nit;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getTelefono() {
        return telefono;
    }

    public void setTelefono(int telefono) {
        this.telefono = telefono;
    }
    
    
    
}
